public class SpecialsCharacters{
    public static void main(String[] args){
        var str1="Hola mundo";

        System.out.println("Cadena original: "+str1);

        //Imprimir el salto de linea '\n'
        str1="Hola\nMundo";
        System.out.println("Cadena con salto de linea: "+str1);

        //Imprimir tabulacion '\t'
        str1="\tHola\tMundo";
        System.out.println("Cadena con salto de linea: "+str1);

        //Imprimir comilla simple \'
        str1="Hola \' Mundo";
        System.out.println("Cadena con comilla simple: "+str1);

        //Imprimir comilla doble\""
        str1="Hola \" Mundo";
        System.out.println("Cadena con comilla simple: "+str1);

        //Imprimir barra invertida\\"
        str1="Hola \\ Mundo";
        System.out.println("Cadena con comilla simple: "+str1);



    }
}