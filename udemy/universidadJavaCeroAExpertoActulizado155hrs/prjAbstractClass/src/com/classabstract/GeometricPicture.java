package prjAbstractClass.src.com.classabstract;

public abstract class GeometricPicture {//Una clase abstracta no se puede instanciar, es decir, no se pueden crear objetos de esta clase, pero si se pueden crear objetos de las clases que heredan de esta clase abstracta.
    
    public abstract void draw(); // Un método abstracto es un método que no tiene una implementación, es decir, no tiene un cuerpo, solo tiene la firma del método.
    
    
}
